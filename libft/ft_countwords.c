unsigned int	ft_countwords(char const *s, char const c)
{
	unsigned int len;

	len = 0;
	while (*s != '\0')
	{
		if ((*s != c && *(s + 1) == c) || (*s != c && *(s + 1) == '\0'))
			len++;
		s++;
	}
	return (len);
}
