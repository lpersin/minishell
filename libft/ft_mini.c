#include "libft.h"

int	ft_mini(int *tab, size_t size)
{
	int		min;
	size_t	i;

	min = tab[0];
	i = 0;
	while (i < size)
	{
		if (min > tab[i])
			min = tab[i];
		i++;
	}
	return (min);
}
